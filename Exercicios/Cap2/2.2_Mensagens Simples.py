#Armazene uma mensagem em uma variável e, em seguida, exiba essa mensagem. Então altere o valor de sua variável para uma nova mensagem e mostre essa nova mensagem.

#Armazenando a primeira mensagem
mensagem_variavel = "Esta é a primeira versão da mensagem."
print(mensagem_variavel)

#alterando o valor da mesma variável
mensagem_variavel = "Agora a variável contém este novo texto!"
print(mensagem_variavel)

#O que aconteceu aqui?

#1. O programa reserva um espaço na memória chamado mensagem_variavel.
#2. Ele guarda o primeiro texto lá e o exibe.
#3. Na linha seguinte, ele descarta o texto antigo e coloca o novo texto no mesmo "espaço".
#4. Quando você dá o segundo print, ele mostra o que está guardado no momento.

